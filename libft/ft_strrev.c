#include "libft.h"

char	*ft_strrev(char *s1)
{
	int		i;
	int		j;
	int		len;
	char	c;

	len = ft_strlen(s1);
	j = len - 1;
	i = 0;
	while (i < j)
	{
		c = s1[i];
		s1[i] = s1[j];
		s1[j] = c;
		j--;
		i++;
	}
	return (s1);
}
